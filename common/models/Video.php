<?php
namespace common\models;

use Yii;
use yii\base\NotSupportedException;
use yii\behaviors\TimestampBehavior;
use yii\db\ActiveRecord;
use yii\web\IdentityInterface;

/**
 * video model
 *
 * @property integer $id
 * @property string $videoname
 * @property string $password_hash
 * @property string $password_reset_token
 * @property string $verification_token
 * @property string $email
 * @property string $auth_key
 * @property integer $status
 * @property integer $created_at
 * @property integer $updated_at
 * @property string $password write-only password
 */
class Video extends ActiveRecord implements IdentityInterface
{
    const STATUS_DELETED = 0;
    const STATUS_INACTIVE = 9;
    const STATUS_ACTIVE = 10;


    /**
     * {@inheritdoc}
     */
    public static function tableName()
    {
        return '{{%video}}';
    }

    /**
     * {@inheritdoc}
     */
    public function behaviors()
    {
        return [
            TimestampBehavior::className(),
        ];
    }

    /**
     * {@inheritdoc}
     */
    public function rules()
    {
        return [
            ['status', 'default', 'value' => self::STATUS_INACTIVE],
            ['status', 'in', 'range' => [self::STATUS_ACTIVE, self::STATUS_INACTIVE, self::STATUS_DELETED]],
        ];
    }

    /**
     * {@inheritdoc}
     */
    public static function findIdentity($id)
    {
        return static::findOne(['id' => $id, 'status' => self::STATUS_ACTIVE]);
    }
    public static function findByVideoTitle($videoTitle)
    {
        return static::findOne(['videoTitle' => $videoTitle]);
    }
    public static function findByCate($cate)
    {
        return static::findOne(['cate' => $cate]);
    }
    public static function findByChannel($channel)
    {
        return static::findOne(['channel' => $channel]);
    }
    public static function findByPublistTime($publistTime)
    {
        return static::findOne(['publistTime' => $publistTime]);
    }
    public static function findByVideoDesc($videoDesc)
    {
        return static::findOne(['videoDesc' => $videoDesc]);
    }
    public static function findByItems($items)
    {
        return static::findOne(['items' => $items]);
    }
